//═════════════════════════════════════════════════════════════════
// 📚 บทเรียน: List (ลิสต์)
//═════════════════════════════════════════════════════════════════
// List = ชุดข้อมูลที่เก็บหลายค่าไว้ในตัวแปลเดียว (เหมือน Array)
// • เข้าถึงโดยใช้ Index เริ่มจาก 0, 1, 2, ...
// • สมาชิกเรียกว่า Element
// • สามารถมีชนิดข้อมูลเดียวหรือหลายชนิด

void main() {
  print('💡 ตัวอย่างการใช้ List ใน Dart\n');

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 1: สร้าง List แบบต่างๆ
  //═══════════════════════════════════════════════════════════════
  print('--- ตัวอย่างที่ 1: สร้าง List แบบต่างๆ ---');

  // แบบ 1: List ที่เก็บข้อมูลชนิดใดก็ได้ (var)
  var mixedList = [1, 2.5, 'Hello', true];
  print('var list: $mixedList');

  // แบบ 2: List ที่กำหนดชนิด (List<int>)
  List<int> numbers = [10, 20, 30, 40, 50];
  print('List<int>: $numbers');

  // แบบ 3: List<String>
  List<String> colors = ['แดง', 'เขียว', 'น้ำเงิน'];
  print('List<String>: $colors');

  // แบบ 4: List<double>
  List<double> prices = [49.99, 99.50, 199.00];
  print('List<double>: $prices');

  // แบบ 5: List<bool>
  List<bool> flags = [true, false, true];
  print('List<bool>: $flags');

  // แบบ 6: List ว่างเปล่า
  List<int> emptyList = [];
  print('Empty list: $emptyList');
  print("");

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 2: เข้าถึงสมาชิกโดยใช้ Index
  //═══════════════════════════════════════════════════════════════
  print('--- ตัวอย่างที่ 2: เข้าถึงสมาชิก (Index) ---');

  List<String> fruits = ['ส้ม', 'แอปเปิ้ล', 'กล้วย', 'องุ่น'];
  print('รายการผลไม้: $fruits');
  print('สมาชิกที่ 0: ${fruits[0]}');  // ส้ม
  print('สมาชิกที่ 1: ${fruits[1]}');  // แอปเปิ้ล
  print('สมาชิกตัวแรก: ${fruits.first}');  // ส้ม
  print('สมาชิกตัวสุดท้าย: ${fruits.last}'); // องุ่น
  print("");

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 3: แก้ไขค่าในสมาชิก
  //═══════════════════════════════════════════════════════════════
  print('--- ตัวอย่างที่ 3: แก้ไขค่า ---');

  List<String> animals = ['หมา', 'แมว', 'หนู', 'นก'];
  print('ก่อนแก้ไข: $animals');

  animals[1] = 'ปลา';  // เปลี่ยน 'แมว' เป็น 'ปลา'
  print('หลังแก้ไข: $animals');
  print("");

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 4: เพิ่มสมาชิกใหม่
  //═══════════════════════════════════════════════════════════════
  print('--- ตัวอย่างที่ 4: เพิ่มสมาชิก ---');

  List<int> scores = [85, 90, 78];
  print('ก่อนเพิ่ม: $scores');

  scores.add(95);  // เพิ่มตัวเดียว
  print('หลัง add(): $scores');

  scores.addAll([88, 92]);  // เพิ่มหลายตัว
  print('หลัง addAll(): $scores');

  // หรือใช้ += 
  scores += [100];
  print('หลัง +=: $scores');
  print("");

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 5: ลบสมาชิก
  //═══════════════════════════════════════════════════════════════
  print('--- ตัวอย่างที่ 5: ลบสมาชิก ---');

  List<String> items = ['ปากกา', 'ดินสอ', 'ยางลบ', 'ไม้บรรทัด'];
  print('ก่อนลบ: $items');

  items.remove('ดินสอ');  // ลบค่าที่ระบุ
  print('หลัง remove(): $items');

  items.removeAt(0);  // ลบตามตำแหน่ง
  print('หลัง removeAt(0): $items');
  print("");

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 6: การใช้ Length
  //═══════════════════════════════════════════════════════════════
  print('--- ตัวอย่างที่ 6: ตรวจสอบจำนวน ---');

  List<String> names = ['สมชาย', 'วิชัย', 'สมปอง'];
  print('รายชื่อ: $names');
  print('จำนวนสมาชิก: ${names.length}');
  print('ว่างหรือไม่? ${names.isEmpty}');

  List<int> emptyNumbers = [];
  print('emptyNumbers ว่างหรือไม่? ${emptyNumbers.isEmpty}');
  print("");

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 7: วนลูปผ่าน List (For Loop)
  //═══════════════════════════════════════════════════════════════
  print('--- ตัวอย่างที่ 7: วนลูป For ---');

  List<int> numbers7 = [10, 20, 30, 40];

  print('แบบ 1: For Loop ปรกติ');
  for (int i = 0; i < numbers7.length; i++) {
    print('  Index $i: ${numbers7[i]}');
  }
  print("");

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 8: วนลูป For-in
  //═══════════════════════════════════════════════════════════════
  print('--- ตัวอย่างที่ 8: วนลูป For-in ---');

  List<String> cities = ['กรุงเทพ', 'เชียงใหม่', 'ขอนแก่น', 'ภูเก็ต'];
  for (String city in cities) {
    print('  - $city');
  }
  print("");

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 9: วนลูป forEach
  //═══════════════════════════════════════════════════════════════
  print('--- ตัวอย่างที่ 9: วนลูป forEach ---');

  List<int> values = [5, 10, 15, 20];
  values.forEach((value) {
    print('  ค่า: $value × 2 = ${value * 2}');
  });
  print("");

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 10: ตรวจสอบความมี contains()
  //═══════════════════════════════════════════════════════════════
  print('--- ตัวอย่างที่ 10: contains() ---');

  List<String> sports = ['ฟุตบอล', 'บาส', 'เทนนิส', 'จักรยาน'];
  print('รายการกีฬา: $sports');
  print('มี "ฟุตบอล" หรือไม่? ${sports.contains('ฟุตบอล')}');
  print('มี "ว่ายน้ำ" หรือไม่? ${sports.contains('ว่ายน้ำ')}');
  print("");

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 11: indexOf() หาตำแหน่ง
  //═══════════════════════════════════════════════════════════════
  print('--- ตัวอย่างที่ 11: indexOf() ---');

  List<String> colors11 = ['แดง', 'เขียว', 'น้ำเงิน', 'เหลือง'];
  print('สี: $colors11');
  print('ตำแหน่งของ "เขียว": ${colors11.indexOf('เขียว')}');
  print('ตำแหน่งของ "ม่วง": ${colors11.indexOf('ม่วง')}'); // -1 แปลว่าไม่มี
  print("");

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 12: insert() แทรกสมาชิก
  //═══════════════════════════════════════════════════════════════
  print('--- ตัวอย่างที่ 12: insert() ---');

  List<String> days = ['จันทร์', 'อังคาร', 'พฤหัสบดี'];
  print('ก่อนแทรก: $days');

  days.insert(1, 'วันนี้คือวันพุธ');  // แทรกตำแหน่ง 1
  print('หลังแทรก: $days');
  print("");

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 13: sort() เรียงลำดับ
  //═══════════════════════════════════════════════════════════════
  print('--- ตัวอย่างที่ 13: sort() ---');

  List<int> randomNumbers = [50, 20, 80, 10, 40];
  print('ก่อนเรียง: $randomNumbers');

  randomNumbers.sort();
  print('หลังเรียง (น้อยไปมาก): $randomNumbers');

  randomNumbers.sort((a, b) => b.compareTo(a));  // มากไปน้อย
  print('เรียงมากไปน้อย: $randomNumbers');
  print("");

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 14: reversed เรียงย้อนกลับ
  //═══════════════════════════════════════════════════════════════
  print('--- ตัวอย่างที่ 14: reversed ---');

  List<String> alphabet = ['A', 'B', 'C', 'D', 'E'];
  print('ปกติ: $alphabet');
  print('ย้อนกลับ: ${alphabet.reversed.toList()}');
  print("");

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 15: join() เชื่อมเป็นสตริง
  //═══════════════════════════════════════════════════════════════
  print('--- ตัวอย่างที่ 15: join() ---');

  List<String> words = ['สวัสดี', 'ชาวไทย', 'อย่างยินดี'];
  String sentence = words.join(' ');
  print('join(" "): $sentence');

  List<int> digits = [1, 2, 3, 4, 5];
  print('join(""): ${digits.join("")}');
  print('join("-"): ${digits.join("-")}');
  print("");

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 16: clear() ลบทั้งหมด
  //═══════════════════════════════════════════════════════════════
  print('--- ตัวอย่างที่ 16: clear() ---');

  List<int> tempList = [1, 2, 3, 4, 5];
  print('ก่อน clear: $tempList');
  tempList.clear();
  print('หลัง clear: $tempList');
  print("");

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 17: List.generate()
  //═══════════════════════════════════════════════════════════════
  print('--- ตัวอย่างที่ 17: List.generate() ---');

  List<int> generated1 = List.generate(5, (index) => index + 1);
  print('List.generate(5, (i) => i+1): $generated1');

  List<String> generated2 = List.generate(3, (index) => 'Item ${index + 1}');
  print('List.generate(3, ...): $generated2');

  List<int> squares = List.generate(5, (i) => (i + 1) * (i + 1));
  print('เลขยกกำลัง 2: $squares');
  print("");

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 18: List ของ List (Nested List)
  //═══════════════════════════════════════════════════════════════
  print('--- ตัวอย่างที่ 18: Nested List ---');

  List<List<int>> matrix = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
  ];

  print('Matrix:');
  for (var row in matrix) {
    print('  $row');
  }
  print('matrix[1][2]: ${matrix[1][2]}');
  print("");

  //═══════════════════════════════════════════════════════════════
  // 📌 สรุปเมทอด List ที่สำคัญ
  //═════════════════════════════════════════════════════════════════
  print('📌 เมทอด List ที่สำคัญ:');
  print('• length = จำนวนสมาชิก');
  print('• first/last = สมาชิกแรก/ท้ายสุด');
  print('• isEmpty = ตรวจสอบว่างไหม');
  print('• add(x) = เพิ่มทีละตัว');
  print('• addAll(list) = เพิ่มหลายตัว');
  print('• remove(x) = ลบค่าที่ระบุ');
  print('• removeAt(i) = ลบตามตำแหน่ง');
  print('• contains(x) = มี x หรือไม่');
  print('• indexOf(x) = ตำแหน่งของ x');
  print('• insert(i, x) = แทรกที่ตำแหน่ง i');
  print('• sort() = เรียงลำดับ');
  print('• reversed = เรียงย้อนกลับ');
  print('• join(sep) = เชื่อมเป็นสตริง');
  print('• forEach(f) = วนลูป');
  print('• clear() = ลบทั้งหมด');
}
