import 'dart:io';

//═════════════════════════════════════════════════════════════════
// 📚 บทเรียน: Map (พจนานุกรม)
//═════════════════════════════════════════════════════════════════
// Map จัดเก็บข้อมูลในลักษณะคู่ key-value (เหมือน Dictionary)
// • Key = ตัวระบุ (หาค่ามีเสมอ)
// • Value = ค่าที่เก็บไว้
//
// ข้อดี:
// • เข้าถึงข้อมูลได้เร็ว โดยใช้ key
// • เหมาะสำหรับข้อมูลที่มีการจับคู่

void main() {
  print('💡 ตัวอย่างการใช้ Map ใน Dart\n');

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 1: สร้าง Map ข้อมูลทั่วไป
  //═══════════════════════════════════════════════════════════════
  // รูปแบบ:
  // Map<ชนิด Key, ชนิด Value> ชื่อ = {key: value, key: value}

  print('--- ตัวอย่างที่ 1: สร้าง Map ---');

  // Map ที่เก็บรหัสประเทศและชื่อประเทศ
  Map<String, String> country = {
    'th': 'ไทย',
    'jp': 'ญี่ปุ่น',
    'kr': 'เกาหลีใต้',
    'us': 'สหรัฐอเมริกา',
  };

  print('ประเทศ: $country');
  print("");

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 2: Map ที่มีชนิดข้อมูลต่างกัน
  //═══════════════════════════════════════════════════════════════

  print('--- ตัวอย่างที่ 2: Map ชนิดต่างๆ ---');

  // Map เก็บราคาสินค้า
  Map<String, int> phone = {'iPhone': 35999, 'Samsung': 39999, 'Nokia': 20000};

  print('ราคาโทรศัพท์: $phone');

  // Map เก็บอายุของคน
  Map<String, int> age = {'สมชาย': 25, 'วิชัย': 30, 'สมปอง': 28};

  print('อายุ: $age');
  print("");

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 3: เข้าถึงข้อมูลโดยใช้ Key
  //═══════════════════════════════════════════════════════════════
  // การเข้าถึง: map[key]

  print('--- ตัวอย่างที่ 3: เข้าถึงข้อมูล ---');
  print('รหัส "th" = ${country['th']}');
  print('ราคา "iPhone" = ${phone['iPhone']} บาท');
  print('อายุของ "วิชัย" = ${age['วิชัย']} ปี');
  print("");

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 4: แก้ไขค่าใน Map
  //═══════════════════════════════════════════════════════════════
  // ใช้ map[key] = ค่าใหม่

  print('--- ตัวอย่างที่ 4: แก้ไขค่า ---');
  print('ก่อนแก้ไข: ${phone['Nokia']}');
  phone['Nokia'] = 25000; // แก้ไขราคา
  print('หลังแก้ไข: ${phone['Nokia']}');
  print("");

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 5: เพิ่มข้อมูลใหม่ใน Map
  //═══════════════════════════════════════════════════════════════

  print('--- ตัวอย่างที่ 5: เพิ่มข้อมูลใหม่ ---');
  print('ก่อนเพิ่ม: $country');

  country['de'] = 'เยอรมัน'; // เพิ่มสมาชิกใหม่
  country['fr'] = 'ฝรั่งเศส';

  print('หลังเพิ่ม: $country');
  print("");

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 6: เรียกดู Keys และ Values
  //═══════════════════════════════════════════════════════════════
  // .keys = ดูคีย์ทั้งหมด
  // .values = ดูค่าทั้งหมด

  print('--- ตัวอย่างที่ 6: Keys และ Values ---');
  print('Keys: ${country.keys}');
  print('Values: ${country.values}');
  print("");

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 7: วนลูปผ่าน Map (For-in)
  //═══════════════════════════════════════════════════════════════

  print('--- ตัวอย่างที่ 7: วนลูปด้วย For-in ---');
  for (String key in country.keys) {
    print('$key = ${country[key]}');
  }
  print("");

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 8: วนลูปผ่าน Map (forEach)
  //═══════════════════════════════════════════════════════════════
  // forEach(key, value) ลูปผ่านแต่ละคู่

  print('--- ตัวอย่างที่ 8: วนลูปด้วย forEach ---');
  phone.forEach((key, value) {
    print('$key มีราคา $value บาท');
  });
  print("");

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 9: ตรวจสอบ Map ว่างหรือไม่
  //═══════════════════════════════════════════════════════════════

  print('--- ตัวอย่างที่ 9: isEmpty และ length ---');
  print('จำนวนประเทศ: ${country.length}');
  print('ว่างหรือไม่? ${country.isEmpty}');

  Map<String, String> emptyMap = {};
  print('emptyMap ว่างหรือไม่? ${emptyMap.isEmpty}');
  print("");

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 10: ตรวจสอบ Key และ Value
  //═══════════════════════════════════════════════════════════════
  // containsKey() = มี key นี้หรือไม่
  // containsValue() = มี value นี้หรือไม่

  print('--- ตัวอย่างที่ 10: containsKey & containsValue ---');
  print('มี key "th" หรือไม่? ${country.containsKey('th')}');
  print('มี key "mx" หรือไม่? ${country.containsKey('mx')}');
  print('มี value "ไทย" หรือไม่? ${country.containsValue('ไทย')}');
  print('มี value "อังกฤษ" หรือไม่? ${country.containsValue('อังกฤษ')}');
  print("");

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 11: ลบข้อมูลออก
  //═══════════════════════════════════════════════════════════════
  // remove(key) = ลบสมาชิกตามที่ระบุ
  // clear() = ลบทั้งหมด

  print('--- ตัวอย่างที่ 11: ลบข้อมูล ---');
  print('ก่อนลบ: $country');

  country.remove('de'); // ลบประเทศเยอรมัน
  print('หลังลบ "de": $country');
  print("");

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 12: เพิ่มหลาย Map
  //═══════════════════════════════════════════════════════════════
  // addAll(otherMap) = รวม Map สองตัวเข้าด้วยกัน

  print('--- ตัวอย่างที่ 12: รวม Map เข้าด้วยกัน ---');
  Map<String, String> country2 = {'it': 'อิตาลี', 'es': 'สเปน'};

  print('country: $country');
  print('country2: $country2');

  country.addAll(country2);
  print('หลังรวม: $country');
  print("");

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 13: Map ของ List
  //═════════════════════════════════════════════════════════════════
  // Map ที่ value เป็น List

  print('--- ตัวอย่างที่ 13: Map ของ List ---');
  Map<String, List<String>> skills = {
    'สมชาย': ['Dart', 'Flutter', 'Java'],
    'วิชัย': ['Python', 'JavaScript'],
    'สมปอง': ['C++', 'Rust'],
  };

  skills.forEach((name, skillList) {
    print('$name มีทักษะ: $skillList');
  });
  print("");

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 14: Map ของ Map
  //═════════════════════════════════════════════════════════════════

  print('--- ตัวอย่างที่ 14: Map ของ Map ---');
  Map<String, Map<String, int>> person = {
    'สมชาย': {'อายุ': 25, 'คะแนน': 85},
    'วิชัย': {'อายุ': 30, 'คะแนน': 92},
  };

  person.forEach((name, info) {
    print('$name: อายุ ${info['อายุ']} ปี คะแนน ${info['คะแนน']}');
  });
  print("");

  //═══════════════════════════════════════════════════════════════
  // ✨ ตัวอย่างที่ 15: ใช้ Map สำหรับแปลงข้อมูล
  //═════════════════════════════════════════════════════════════════

  print('--- ตัวอย่างที่ 15: Map สำหรับแปลงเลข ---');
  Map<int, String> gradeMap = {1: 'F', 2: 'D', 3: 'C', 4: 'B', 5: 'A'};

  print('1 = ${gradeMap[1]}');
  print('5 = ${gradeMap[5]}');
  print("");

  //═══════════════════════════════════════════════════════════════
  // 📌 สรุปเมทอด Map ที่สำคัญ
  //═════════════════════════════════════════════════════════════════
  print('📌 เมทอด Map ที่สำคัญ:');
  print('• length = จำนวนสมาชิก');
  print('• isEmpty = ตรวจสอบว่างไหม');
  print('• keys = ดูคีย์ทั้งหมด');
  print('• values = ดูค่าทั้งหมด');
  print('• containsKey(k) = มี key นี้หรือไม่');
  print('• containsValue(v) = มี value นี้หรือไม่');
  print('• remove(key) = ลบสมาชิก');
  print('• clear() = ลบทั้งหมด');
  print('• addAll(map) = รวม Map');
  print('• forEach((k, v) => ...) = วนลูป');
}
