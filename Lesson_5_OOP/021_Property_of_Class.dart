/*
╔═══════════════════════════════════════════════════════════════════════════════╗
║                  📖 Property (คุณลักษณะ/Attribute) คืออะไร?                 ║
╠═══════════════════════════════════════════════════════════════════════════════╣

 นิยาม: ตัวแปรที่ประกาศภายใน Class ซึ่งใช้เก็บข้อมูลของ Object
 ทุก Object ที่สร้างมาจาก Class เดียวกัน จะมี Property ชุดเดียวกัน
 แต่ค่าของ Property อาจแตกต่างกันไปในแต่ละ Object

 ตัวอย่าง:
   Class Person มี Property: name, age, email
   Object person1: name='สมชาย', age=25, email='a@b.com'
   Object person2: name='วิชัย', age=30, email='c@d.com'
   (Property ชุดเดียวกัน แต่ค่าต่างกัน)

 🎯 ประเภท Property:
   1. Property ที่มีค่า default (ได้ทันที)
   2. Property ที่เป็น Nullable (อาจเป็น null)
   3. Property ที่ใช้ late (กำหนดค่าทีหลัง)

╚═══════════════════════════════════════════════════════════════════════════════╝

════════════════════════════════════════════════════════════════════════════════
                     🏗️ ตัวอย่าง: ประเภทของ Property
════════════════════════════════════════════════════════════════════════════════
*/

//───────────────────────────────────────────────────────────────────────────────
// ✨ ตัวอย่างที่ 1: Property ที่มีค่า Default
//───────────────────────────────────────────────────────────────────────────────

class Employee {
  int id = 0; // ← มีค่า default = 0
  String name = ''; // ← มีค่า default = ''
  double salary = 0.0; // ← มีค่า default = 0.0
  String? email; // ← Nullable (สามารถเป็น null ได้)
}

/*
💡 อธิบาย:
   • int id = 0;        → property id เริ่มต้นเป็น 0
   • String name = '';  → property name เริ่มต้นเป็นเว้นว่าง
   • String? email;     → nullable string (เป็น null ได้)
                          ไม่ต้องกำหนด default
*/

//───────────────────────────────────────────────────────────────────────────────
// ✨ ตัวอย่างที่ 2: Property ที่ใช้ late (Lazy Initialization)
//───────────────────────────────────────────────────────────────────────────────

class Car {
  late String brand; // ← จะกำหนดค่าทีหลัง (ไม่ได้เสมอ)
  late String color; // ← ต้องกำหนด ก่อนใช้งาน
  late int year;
}

/*
💡 ทำไมต้องใช้ late?
   • ใช้เมื่อไม่สามารถกำหนดค่า default ได้
   • ต้องรับค่าจากข้างนอกหรือคำนวณทีหลัง
   • ต้องกำหนดค่าอย่างน้อยครั้งเดียวก่อนใช้
   • ถ้าใช้โดยไม่กำหนด → Error!
*/

//───────────────────────────────────────────────────────────────────────────────
// ✨ ตัวอย่างที่ 3: การเข้าถึง Property
//───────────────────────────────────────────────────────────────────────────────

/*
📌 Dot Notation (จุดเพื่อเข้าถึง Property)
   ═══════════════════════════════════════════

   instance.property = ค่า    // ✍️ กำหนดค่า (Write)
   print(instance.property)   // 📖 อ่านค่า (Read)

   ตัวอย่าง:
   ────────
   var emp = Employee();
   emp.id = 101;              // ← กำหนด id = 101
   emp.name = 'สมชาย';       // ← กำหนด name = 'สมชาย'
   print(emp.id);             // ← อ่าน และแสดง: 101
   print(emp.name);           // ← อ่าน และแสดง: สมชาย
*/

//───────────────────────────────────────────────────────────────────────────────
// ✨ ตัวอย่างที่ 4: Cascade Operator (..) - ตัวช่วยสำคัญ!
//───────────────────────────────────────────────────────────────────────────────

/*
📌 Cascade Operator (..) เมื่ออยากกำหนด Property หลาย ๆ ตัวพร้อมกัน
   ═════════════════════════════════════════════════════════════════

   ❌ ไม่ใช้ Cascade (ซ้ำ instance):
   ─────────────────────────────────
   var emp = Employee();
   emp.id = 101;
   emp.name = 'สมชาย';
   emp.salary = 50000;
   emp.email = 'a@b.com';
   // → ต้องเขียน emp. ถึง 5 ครั้ง!

   ✅ ใช้ Cascade (สะอาด):
   ─────────────────────────────────
   var emp = Employee()
     ..id = 101
     ..name = 'สมชาย'
     ..salary = 50000
     ..email = 'a@b.com';
   // → เขียน instance เพียง 1 ครั้ง!

   💡 สิ่งสำคัญ:
      • ใช้ .. (จุด 2 ตัว)
      • ใช้ได้เมื่อสร้าง Object
      • ต้องปิดท้ายด้วย ;
      • ทำให้โค้ดสะอาด ย่อ ไม่ซ้ำซาก
*/

