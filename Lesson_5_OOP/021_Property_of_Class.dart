/*
✅ สิ่งที่สำคัญ:

1️⃣ PROPERTY (คุณลักษณะ)
   - ตัวแปรที่ประกาศใน Class
   - เก็บข้อมูลของ Object
   - ต้องกำหนด Type

2️⃣ DEFAULT VALUE
   - ต้องกำหนดค่า Default ให้ Property ทั้งหมด
   - ยกเว้น Nullable type (String? สามารถเป็น null)
   - ตัวอย่าง: int id = 0; String name = '';

3️⃣ NULLABLE TYPE
   - ใช้ ? เพื่อระบุว่าสามารถเป็น null ได้
   - String? email;  // อาจเป็น null ได้
   - int? age;       // อาจเป็น null ได้
   - ไม่ต้องกำหนด default value

4️⃣ LATE KEYWORD
   - ใช้เมื่อกำหนดค่าทีหลัง
   - late String brand; // จะกำหนดค่าทีหลัง
   - ต้องกำหนดค่าก่อนใช้งาน

5️⃣ DOT NOTATION
   - เข้าถึง Property ด้วย .
   - emp.name = 'สมชาย';  // เขียน
   - print(emp.name);      // อ่าน

6️⃣ CASCADE OPERATOR (..)
   - ใช้ .. สำหรับกำหนด Property หลาย ๆ ตัว
   - var emp = Employee()
       ..id = 101
       ..name = 'สมชาย'
       ..salary = 50000;
   - ทำให้โค้ดสะอาดและไม่ซ้ำซาก

✨ ประเภทของ Property:
   ┌─────────────────────────────────────────────┐
   │ Type              │ Default  │ ต้อง Init?    │
   ├─────────────────────────────────────────────┤
   │ int               │ = 0      │ ใช่           │
   │ String            │ = ''     │ ใช่           │
   │ double            │ = 0.0    │ ใช่           │
   │ bool              │ = false  │ ใช่           │
   │ String?           │ null     │ ไม่           │
   │ int?              │ null     │ ไม่           │
   │ late String       │ (none)   │ ทีหลัง        │
   └─────────────────────────────────────────────┘
*/

