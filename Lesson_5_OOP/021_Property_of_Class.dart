// 🎯 Main Program - ตัวอย่างสมบูรณ์
//═══════════════════════════════════════════════════════════════════════════════

void main() {
  print('═══════════════════════════════════════════════════\n');
  print('📚 Lesson 021: Property ของ Class\n');
  print('═══════════════════════════════════════════════════\n');

  //━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  // 1️⃣ Property ที่มี Default Value
  //━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  print('1️⃣ Property ที่มี Default Value:');
  print('─────────────────────────────\n');

  var emp1 = Employee();
  print('📌 ค่า Default ของ Employee:');
  print('   id: ${emp1.id}');
  print('   name: "${emp1.name}"');
  print('   salary: ${emp1.salary}');
  print('   email: ${emp1.email}\n');

  emp1.id = 101;
  emp1.name = 'สมชาย';
  emp1.salary = 50000;
  emp1.email = 'somchai@company.com';

  print('📌 หลังกำหนดค่า:');
  print('   id: ${emp1.id}');
  print('   name: ${emp1.name}');
  print('   salary: ${emp1.salary}');
  print('   email: ${emp1.email}\n');

  //━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  // 2️⃣ Cascade Operator (..)
  //━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  print('2️⃣ Cascade Operator (..):');
  print('──────────────────────────\n');
  print('✨ สร้างและกำหนด Property พร้อมกันโดยใช้ ..\n');

  var emp2 = Employee()
    ..id = 102
    ..name = 'วิชัย'
    ..salary = 60000
    ..email = 'wichai@company.com';

  print('📌 ข้อมูล Employee 2:');
  print('   id: ${emp2.id}');
  print('   name: ${emp2.name}');
  print('   salary: ${emp2.salary}');
  print('   email: ${emp2.email}\n');

  //━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  // 3️⃣ Late Keyword
  //━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  print('3️⃣ Late Keyword (กำหนดค่าทีหลัง):');
  print('───────────────────────────────\n');

  var car = Car();
  print('📝 กำหนด Property ของ Car:\n');

  car.brand = 'Toyota';
  car.color = 'สีแดง';
  car.year = 2024;

  print('📌 ข้อมูล Car:');
  print('   brand: ${car.brand}');
  print('   color: ${car.color}');
  print('   year: ${car.year}\n');

  //━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  // 4️⃣ Null Safety กับ Nullable Property
  //━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  print('4️⃣ Null Safety - Nullable Property:');
  print('──────────────────────────────────\n');

  var emp3 = Employee();
  emp3.id = 103;
  emp3.name = 'สมปอง';
  emp3.salary = 55000;
  // emp3.email ไม่ได้กำหนด → เป็น null

  print('📌 ข้อมูล Employee 3:');
  print('   id: ${emp3.id}');
  print('   name: ${emp3.name}');
  print('   salary: ${emp3.salary}');
  print('   email: ${emp3.email} (null = ไม่มีข้อมูล)\n');

  //━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  // 5️⃣ ตรวจสอบความรุ่ง
  //━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  print('5️⃣ ตรวจสอบเงินเดือน:');
  print('──────────────────\n');

  if (emp1.salary >= 50000) {
    print('✅ ${emp1.name} มีเงินเดือนดี: ${emp1.salary} บาท');
  } else {
    print('❌ ${emp1.name} มีเงินเดือนต่ำ: ${emp1.salary} บาท');
  }

  if (emp2.salary > emp1.salary) {
    print('✅ ${emp2.name} มีเงินเดือนมากกว่า ${emp1.name}\n');
  }

  print('═══════════════════════════════════════════════════\n');
  print('✅ เสร็จสิ้น!\n');
}

