// Class: Template สำหรับ Person
class Person {
  String name = '';
  int age = 0;

  // Method
  void display() {
    print('ชื่อ: $name, อายุ: $age ปี');
  }
}

//───────────────────────────────────────────────────────────────────────────────
// ✨ ตัวอย่างที่ 2: BankAccount Class
//───────────────────────────────────────────────────────────────────────────────

class BankAccount {
  String accountNumber = '';
  double balance = 0.0;

  void deposit(double amount) {
    balance += amount;
    print('ฝากเงิน: $amount บาท | ยอดเงิน: $balance');
  }

  void withdraw(double amount) {
    if (amount <= balance) {
      balance -= amount;
      print('ถอนเงิน: $amount บาท | ยอดเงิน: $balance');
    } else {
      print('ยอดเงินไม่พอ');
    }
  }
}

//───────────────────────────────────────────────────────────────────────────────
// 🎯 Main Program
//───────────────────────────────────────────────────────────────────────────────

void main() {
  print('═══════════════════════════════════════════════════\n');
  print('📚 Lesson 021: Class และ Object\n');
  print('═══════════════════════════════════════════════════\n');

  //━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  // 1️⃣ การสร้าง Instance พื้นฐาน
  //━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  print('1️⃣ การสร้าง Instance (Object):');
  print('───────────────────────────────\n');
  print(
    '📝 ทั้งสามตัวอย่างต่อไปนี้สร้าง Instance เดียวกัน แค่เขียนแบบต่างกัน:\n',
  );

  // วิธีที่ 1: ระบุ Type ชัดเจน
  print('  ✅ วิธีที่ 1: Person ps = Person();');
  Person ps = Person();
  ps.name = 'สมชาย';
  ps.age = 25;
  ps.display(); // ชื่อ: สมชาย, อายุ: 25 ปี
  print('');

  // วิธีที่ 2: ใช้ var (อัตโนมัติจำ type)
  print('  ✅ วิธีที่ 2: var ps2 = Person();');
  var ps2 = Person();
  ps2.name = 'วิชัย';
  ps2.age = 30;
  ps2.display(); // ชื่อ: วิชัย, อายุ: 30 ปี
  print('');

  // วิธีที่ 3: ใช้ final (ไม่สามารถกำหนด reference ใหม่ได้)
  print('  ✅ วิธีที่ 3: final ps3 = Person();');
  final ps3 = Person();
  ps3.name = 'สมปอง';
  ps3.age = 28;
  ps3.display(); // ชื่อ: สมปอง, อายุ: 28 ปี
  print('');
  print('  💡 ความแตกต่าง:');
  print('     • Person ps: ต้องระบุ Type เสมอ');
  print('     • var ps: Dart บอกเองว่า Type คืออะไร');
  print(
    '     • final ps: ไม่สามารถเปลี่ยน reference ได้ (แต่เปลี่ยน Property ได้)\n',
  );

  //━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  // 2️⃣ BankAccount Example
  //━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  print('2️⃣ BankAccount Example (Property + Method):');
  print('──────────────────────────────────────────\n');
  print('📝 ตัวอย่างการใช้ Property (ข้อมูล) และ Method (พฤติกรรม):\n');

  BankAccount account = BankAccount();
  account.accountNumber = '123456789';
  account.balance = 5000;

  print('💰 บัญชี: ${account.accountNumber}');
  print('💵 ยอดเงินเริ่มต้น: ${account.balance} บาท\n');

  print('📌 การใช้ Method:');
  account.deposit(2000); // ฝากเงิน (Property เปลี่ยนภายใน Method)
  account.withdraw(1500); // ถอนเงิน (สำเร็จ)
  account.withdraw(10000); // ถอนเกิน (ล้มเหลว มี if ตรวจสอบ)
  print('');

  //━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  // 3️⃣ สำคัญ: Instance ไม่ซ้ำกัน
  //━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  print('3️⃣ Instance ไม่ซ้ำกัน (แต่ละตัวเป็นอิสระ):');
  print('──────────────────────────────────────\n');
  print('💡 สำคัญ: เปลี่ยน Instance หนึ่ง ไม่ได้ผลต่ออีก Instance!\n');

  Person p1 = Person();
  Person p2 = Person();

  p1.name = 'สมชาย';
  p2.name = 'วิชัย';

  print('📍 p1.name = ${p1.name}');
  print('📍 p2.name = ${p2.name}');
  print('\n📝 ภาพในหน่วยความจำ:');
  print('   p1 ──→ [Person Object] ← p2');
  print('         name: สมชาย          name: วิชัย');
  print('         age: 0               age: 0');
  print('   ⬆ Instance ต่างกัน ⬆\n');
  print('💭 อธิบาย: p1 และ p2 ชี้ไป Object ต่างกัน');
  print('   ดังนั้น เปลี่ยน p1.name ไม่ได้ผลต่อ p2.name!\n');

  print('═══════════════════════════════════════════════════\n');
  print('✅ เสร็จสิ้น!\n');
}

