/*
╔═══════════════════════════════════════════════════════════════════════════════╗
║                          ✅ ความเข้าใจ Class vs Instance                     ║
╠═══════════════════════════════════════════════════════════════════════════════╣
║                                                                               ║
║  📌 CLASS = สิ่งที่กำหนดไว้ (เพียงแค่แบบร่าง)                                ║
║     ─────────────────────────────────────────                                ║
║     class Person {                                                           ║
║       String name = '';     // ไม่มีข้อมูลจริง                              ║
║       int age = 0;          // เพียงแค่โครงสร้าง                            ║
║       void display() { }    // เพียงแค่ว่างเปล่า                            ║
║     }                                                                        ║
║     ⓘ คิดว่า: Class = สูตรอาหาร (บอกว่าต้องใช้ส่วนไหนบ้าง)               ║
║                                                                               ║
║  📌 INSTANCE = ตัวจริง (สร้างมาจาก Class)                                   ║
║     ─────────────────────────────────────────                                ║
║     Person ps = Person();  // สร้าง Instance จาก Class                      ║
║     ps.name = 'สมชาย';      // มีข้อมูลจริง                                 ║
║     ps.age = 25;            // มีค่า concrete                                ║
║     ⓘ คิดว่า: Instance = อาหารจริง ๆ (ทำจากสูตร)                          ║
║                                                                               ║
║  📌 สร้างหลาย Instance ได้                                                  ║
║     ─────────────────────────────────────────                                ║
║     Person p1 = Person();  // Instance ที่ 1                                ║
║     p1.name = 'สมชาย';                                                      ║
║                                                                               ║
║     Person p2 = Person();  // Instance ที่ 2                                ║
║     p2.name = 'วิชัย';      // ข้อมูลต่างกัน แต่มาจาก Class เดียวกัน       ║
║                                                                               ║
║     Person p3 = Person();  // Instance ที่ 3 ฯลฯ                           ║
║     ⓘ 1 Class → หลาย Instance                                               ║
║                                                                               ║
║  📌 Property vs Method                                                       ║
║     ─────────────────────────────────────────                                ║
║     Property = ข้อมูล (Noun)        Method = พฤติกรรม (Verb)                ║
║     ├─ name: 'สมชาย'                ├─ display()                            ║
║     ├─ age: 25                       ├─ speak()                              ║
║     └─ email: 'a@b.com'             └─ walk()                               ║
║                                                                               ║
║  📌 การเข้าถึง (Dot Notation)                                                ║
║     ─────────────────────────────────────────                                ║
║     instance.property      // อ่าน/เขียน Property                            ║
║     instance.method()      // เรียก Method                                   ║
║                                                                               ║
║     ตัวอย่าง:                                                                ║
║     ps.name = 'สมชาย';             // เขียน property                        ║
║     print(ps.name);                 // อ่าน property                         ║
║     ps.display();                   // เรียก method                          ║
║                                                                               ║
