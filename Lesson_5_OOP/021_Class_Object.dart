╔═══════════════════════════════════════════════════════════════════════════════╗
║                        ✅ ทำไมต้องใช้ OOP (Class/Instance)?                  ║
╠═══════════════════════════════════════════════════════════════════════════════╣
║                                                                               ║
║  ❌ แบบเก่า (ไม่ใช้ OOP):                                                    ║
║     ─────────────────────────────────────────                                ║
║     String name1 = 'สมชาย';   // ตัวแปรกระจาย                              ║
║     int age1 = 25;             // ยากต่อการจัดการ                           ║
║     String name2 = 'วิชัย';                                                 ║
║     int age2 = 30;                                                          ║
