/*
╔═══════════════════════════════════════════════════════════════════════════════╗
║                  📖 บทนิยาม (Definitions) - ตามหนังสือเรียน                   ║
╠═══════════════════════════════════════════════════════════════════════════════╣
║                                                                               ║
║  🎯 CLASS (คลาส)                                                             ║
║  ┌───────────────────────────────────────────────────────────────────────┐   ║
║  │  นิยาม: โครงร่างหรือแบบแผนที่นิยามลักษณะเฉพาะและพฤติกรรมทั่วไปของ     │   ║
║  │  วัตถุ (Object) ที่มีลักษณะเหมือนกัน คลาสจะระบุ:                   │   ║
║  │  1. คุณลักษณะ (Attributes/Properties): ข้อมูลที่แสดงสถานะของวัตถุ    │   ║
║  │  2. วิธีการ (Methods): การกระทำหรือพฤติกรรมที่วัตถุสามารถทำได้     │   ║
║  │                                                                         │   ║
║  │  สัญลักษณ์ทั่วไป: class ชื่อคลาส { คุณลักษณะ; วิธีการ; }          │   ║
║  │  ตัวอย่าง: class Person { String name; void speak(); }              │   ║
║  └───────────────────────────────────────────────────────────────────────┘   ║
║                                                                               ║
║  📦 OBJECT (ออบเจกต์) หรือ INSTANCE (อินสตานซ์)                            ║
║  ┌───────────────────────────────────────────────────────────────────────┐   ║
║  │  นิยาม: ตัวตัวอย่างจริง (concrete entity) ที่สร้างขึ้นจากคลาส        │   ║
║  │  วัตถุประกอบด้วย:                                                    │   ║
║  │  1. สถานะ (State): ค่าปัจจุบันของคุณลักษณะทั้งหมด                  │   ║
║  │  2. พฤติกรรม (Behavior): การกระทำที่สามารถดำเนินการได้               │   ║
║  │                                                                         │   ║
║  │  การสร้างวัตถุ: Person person1 = Person();                           │   ║
║  │  (นำชื่อคลาสมากำหนดเป็นชนิดข้อมูล แล้วใช้ Constructor สร้างวัตถุ)   │   ║
║  └───────────────────────────────────────────────────────────────────────┘   ║
║                                                                               ║
║  🔗 ความสัมพันธ์ระหว่าง Class กับ Object                                   ║
║  ┌───────────────────────────────────────────────────────────────────────┐   ║
║  │  • Class คือสิ่งที่นิยาม (สิ่งเดียว)                                │   ║
║  │  • Object คือการใช้งาน (หลายตัว)                                   │   ║
║  │  • Class = Template; Object = Instance สร้างจาก Template            │   ║
║  │  • 1 Class สามารถสร้าง Object ได้หลายตัว                          │   ║
║  │  • Object แต่ละตัวมีสถานะเฉพาะตัว (ค่า Property ต่างกัน)         │   ║
║  │                                                                         │   ║
║  │  ตัวอย่าง:                                                            │   ║
║  │    Class Person = บัญชีอธิบายว่าคนมี name, age, และสามารถ speak()   │   ║
║  │    Object person1 = คนชื่อ "สมชาย" อายุ 25 ปี                      │   ║
║  │    Object person2 = คนชื่อ "วิชัย" อายุ 30 ปี                      │   ║
║  │    (ทั้ง person1 และ person2 เป็น Instance ของ Class Person)       │   ║
║  └───────────────────────────────────────────────────────────────────────┘   ║
║                                                                               ║
╚═══════════════════════════════════════════════════════════════════════════════╝

