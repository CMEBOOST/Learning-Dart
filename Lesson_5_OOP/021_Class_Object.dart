                          🔍 ตัวอย่างการนำไปใช้
════════════════════════════════════════════════════════════════════════════════

📌 องค์ประกอบของ Object:
   1. Property (คุณสมบัติ): ข้อมูลของวัตถุ เช่น สี, ราคา, ยี่ห่อ
   2. Method (วิธี/พฤติกรรม): การกระทำ เช่น สตาร์ท, เร่ง, หยุด

📌 ตัวอย่างจริง - รถยนต์:
   🚗 Class = แบบพิมพ์เขียว (Blueprint) ของรถยนต์
      - สเปค: สีไหน, เครื่องยนต์, ความจุ
      - ความสามารถ: เดิน, หยุด, เร่ง
   
   🚗 Object/Instance = รถยนต์ตัวจริง
      - รถคันแรก: สีแดง, เครื่อง 2000cc
      - รถคันสอง: สีดำ, เครื่อง 1500cc
      - ทั้งสอง Instance มาจาก Class เดียวกัน แต่ข้อมูลต่างกัน

╔═══════════════════════════════════════════════════════════════════════════════╗
║                          🏗️ Class = Template/Blueprint                        ║
╠═══════════════════════════════════════════════════════════════════════════════╣
║                                                                               ║
║   class Person {                   ← Class ชื่อ "Person" (ตัวพิมพ์ใหญ่)      ║
║     String name;        ← Property   |_ ข้อมูลของคน (คุณสมบัติ)             ║
║     int age;            ← Property   |                                        ║
║                                                                               ║
║     void display() {    ← Method     |_ สิ่งที่คนสามารถทำได้ (พฤติกรรม)     ║
║       print('...');                                                           ║
║     }                                                                         ║
║   }                                                                           ║
║                                                                               ║
╚═══════════════════════════════════════════════════════════════════════════════╝

╔═══════════════════════════════════════════════════════════════════════════════╗
║           📦 Instance/Object = ตัวอย่างจริง (สร้างจาก Class)                 ║
╠═══════════════════════════════════════════════════════════════════════════════╣
║                                                                               ║
║   Person ps = Person();  ← สร้าง Instance ชื่อ "ps" จาก Class "Person"     ║
║   ⬆      ⬆      ⬆⬆⬆⬆⬆  ⬆⬆⬆⬆⬆⬆⬆⬆                                         ║
║   Type  ชื่อ   Constructor                                                    ║
║   อ้างอิง       (สร้างวัตถุ)                                                  ║
║                                                                               ║
║   ps.name = 'สมชาย';    ← ตั้งค่า Property                                   ║
║   ps.age = 25;          ← ตั้งค่า Property                                   ║
║   ps.display();         ← เรียก Method                                        ║
║                                                                               ║
╚═══════════════════════════════════════════════════════════════════════════════╝

╔═══════════════════════════════════════════════════════════════════════════════╗
║                    🔍 Class vs Instance (ภาพเปรียบเทียบ)                     ║
╠═══════════════════════════════════════════════════════════════════════════════╣
║                                                                               ║
║   CLASS (Template)           │   INSTANCE (ตัวอย่าง)                         ║
║   ─────────────────────────  │   ──────────────────────                       ║
║   Person                     │   ps = Person()       (Instance 1)            ║
║   ├─ name: String            │   └─ name: 'สมชาย'                           ║
║   ├─ age: int                │   └─ age: 25                                  ║
║   └─ display()               │                                                ║
║                              │   ps2 = Person()      (Instance 2)            ║
║   (แบบร่าง)                  │   ├─ name: 'วิชัย'                           ║
║   (ไม่มีข้อมูลจริง)          │   └─ age: 30                                  ║
║                              │                                                ║
║                              │   (ข้อมูลต่างกัน แต่อยู่ใน Class เดียวกัน)   ║
║                                                                               ║
╚═══════════════════════════════════════════════════════════════════════════════╝

╔═══════════════════════════════════════════════════════════════════════════════╗
║                     🧠 Memory Reference (ทำไมต้องใช้ Instance)                ║
╠═══════════════════════════════════════════════════════════════════════════════╣
║                                                                               ║
║   ps1 = Person()          ps2 = Person()                                      ║
║    │                       │                                                  ║
║    └──→ [Object in Memory] └──→ [Object in Memory]                           ║
║        name: 'สมชาย'           name: 'วิชัย'                                ║
║        age: 25                age: 30                                         ║
║                                                                               ║
║   ps1 และ ps2 เป็น "reference" (อ้างอิง)                                     ║
║   พวกมันชี้ไปยัง Object ต่างกันใน Memory                                     ║
║   เปลี่ยน ps1 ไม่ได้ผลต่อ ps2 เพราะเป็นคนละที่ใน Memory!                     ║
║                                                                               ║
╚═══════════════════════════════════════════════════════════════════════════════╝

✨ สรุป:
   • Class = เป็นสิ่งเดียว (ใช้ร่วมกัน)
   • Instance = หลายตัว (แต่ละตัวเป็นอิสระ)
*/

